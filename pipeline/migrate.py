"""
Migrate flat datasets.csv → three-table schema (entities / claims / sources).

Usage:
    python -m pipeline.migrate                    # dry-run, print summary
    python -m pipeline.migrate --apply            # write data/ files
    python -m pipeline.migrate --apply --backup   # write + timestamped backup

Output files (in data/):
    entities.csv
    claims.csv
    sources.csv
    quarantine.csv   -- rows that failed validation during migration

The flat datasets.csv remains as a READ-ONLY legacy view; after migration
it is regenerated by pipeline/publish.py rather than hand-edited.
"""
from __future__ import annotations

import argparse
import csv
import hashlib
import sys
import os
from datetime import date
from pathlib import Path

ROOT = Path(__file__).parent.parent
sys.path.insert(0, str(ROOT))

from schema.schema import Entity, Claim, Source, ENTITY_COLUMNS, CLAIM_COLUMNS, SOURCE_COLUMNS
from schema.predicates import (
    ALL_PREDICATES, YEAR, STIMULUS_MODALITY, POPULATION,
    N_SUBJECTS, N_UNIQUE_STIMULI, STIMULUS_REPEATS, HOURS_PER_SUBJECT,
    FIELD_STRENGTH, TR_SECONDS, BIDS_COMPLIANT, ACCESS, LICENCE,
    STIMULUS_LICENCE, URL, DOI, USED_IN, NOTES,
    STIMULUS_MODALITY_VALUES, POPULATION_VALUES, ACCESS_VALUES,
    BIDS_VALUES, SOURCED_PREDICATES,
)

TODAY = date.today().isoformat()
DATA_DIR = ROOT / "data"

# Map flat CSV column → predicate name (most are 1:1, listed for explicitness)
COL_TO_PREDICATE = {
    "year": YEAR,
    "stimulus_modality": STIMULUS_MODALITY,
    "population": POPULATION,
    "n_subjects": N_SUBJECTS,
    "n_unique_stimuli": N_UNIQUE_STIMULI,
    "stimulus_repeats": STIMULUS_REPEATS,
    "hours_per_subject": HOURS_PER_SUBJECT,
    "field_strength": FIELD_STRENGTH,
    "tr_seconds": TR_SECONDS,
    "bids_compliant": BIDS_COMPLIANT,
    "access": ACCESS,
    "licence": LICENCE,
    "stimulus_licence": STIMULUS_LICENCE,
    "url": URL,
    "doi": DOI,
    "used_in": USED_IN,
    "notes": NOTES,
}

# Predicates whose source is the landing page URL of the dataset itself
# (not separately evidenced — confidence=medium is appropriate)
SELF_EVIDENCED = frozenset({
    STIMULUS_MODALITY, POPULATION, BIDS_COMPLIANT, FIELD_STRENGTH,
    TR_SECONDS, STIMULUS_REPEATS, USED_IN, NOTES,
})


def _source_id(url: str) -> str:
    return hashlib.sha256(url.encode()).hexdigest()[:8]


def _infer_confidence(predicate: str, value: str, entity_url: str) -> str:
    """Initial confidence assignment during migration from flat CSV.

    All migrated claims start as 'medium' because the flat CSV had no
    source URLs — we know the landing page exists but didn't verify
    individual field values against primary papers.

    Claims with a doi field start as 'high' (doi evidences year + access).
    """
    if predicate in SOURCED_PREDICATES and value not in ("-", ""):
        return "medium"
    return "low" if value in ("-", "") else "medium"


def migrate(flat_csv: Path, apply: bool, backup: bool) -> dict:
    entities: list[Entity] = []
    all_claims: list[Claim] = []
    source_map: dict[str, Source] = {}  # url → Source
    quarantine: list[dict] = []

    with flat_csv.open(newline="", encoding="utf-8") as f:
        rows = list(csv.DictReader(f))

    for row in rows:
        entity_id = row["id"].strip()
        name = row["name"].strip()
        entity_url = row["url"].strip()

        if not entity_id or not name:
            quarantine.append({**row, "_reason": "missing id or name"})
            continue

        # Validate key enum fields
        warnings = []
        if row.get("stimulus_modality") and row["stimulus_modality"] not in STIMULUS_MODALITY_VALUES:
            warnings.append(f"unknown stimulus_modality={row['stimulus_modality']!r}")
        if row.get("population") and row["population"] not in POPULATION_VALUES:
            warnings.append(f"unknown population={row['population']!r}")
        if row.get("access") and row["access"] not in ACCESS_VALUES:
            warnings.append(f"unknown access={row['access']!r}")

        if warnings:
            quarantine.append({**row, "_reason": "; ".join(warnings)})
            # Still migrate, just flag it
            print(f"  WARN [{entity_id}]: {'; '.join(warnings)}", file=sys.stderr)

        entities.append(Entity(entity_id=entity_id, name=name, url=entity_url))

        # Register the landing page as a source
        if entity_url:
            if entity_url not in source_map:
                source_map[entity_url] = Source(
                    source_id=_source_id(entity_url),
                    url=entity_url,
                    retrieved_at=TODAY,
                    status="unverified",
                )
            landing_source_id = source_map[entity_url].source_id
        else:
            landing_source_id = ""

        doi_val = row.get("doi", "").strip()
        doi_source_id = ""
        if doi_val:
            doi_url = f"https://doi.org/{doi_val}"
            if doi_url not in source_map:
                source_map[doi_url] = Source(
                    source_id=_source_id(doi_url),
                    url=doi_url,
                    retrieved_at=TODAY,
                    status="unverified",
                )
            doi_source_id = source_map[doi_url].source_id

        # Emit one claim per predicate column
        for col, predicate in COL_TO_PREDICATE.items():
            value = (row.get(col) or "").strip()
            if not value:
                continue

            # Source assignment strategy for migrated data:
            # - doi predicate → evidenced by itself (the doi IS the source)
            # - access, licence, url, year → doi (if present) else landing page
            # - structural / editorial → landing page
            if predicate == DOI:
                source_ids = [landing_source_id] if landing_source_id else []
            elif predicate in SOURCED_PREDICATES and doi_source_id:
                source_ids = [doi_source_id]
            elif landing_source_id:
                source_ids = [landing_source_id]
            else:
                source_ids = []

            confidence = _infer_confidence(predicate, value, entity_url)

            claim = Claim(
                claim_id=f"{entity_id}__{predicate}",
                entity_id=entity_id,
                predicate=predicate,
                value=value,
                source_ids=source_ids,
                confidence=confidence,
                retrieved_at=TODAY,
            )
            all_claims.append(claim)

    stats = {
        "entities": len(entities),
        "claims": len(all_claims),
        "sources": len(source_map),
        "quarantine": len(quarantine),
    }

    if not apply:
        print("\nDry-run summary:")
        for k, v in stats.items():
            print(f"  {k}: {v}")
        print("\nRe-run with --apply to write data/ files.")
        return stats

    DATA_DIR.mkdir(exist_ok=True)

    if backup:
        import shutil
        ts = TODAY.replace("-", "")
        for f in ("entities.csv", "claims.csv", "sources.csv"):
            src = DATA_DIR / f
            if src.exists():
                shutil.copy(src, DATA_DIR / f"{ts}_{f}.bak")
        print("Backups written.")

    # Write entities.csv
    with (DATA_DIR / "entities.csv").open("w", newline="", encoding="utf-8") as f:
        w = csv.DictWriter(f, fieldnames=ENTITY_COLUMNS)
        w.writeheader()
        for e in entities:
            w.writerow({"entity_id": e.entity_id, "name": e.name, "url": e.url})

    # Write sources.csv — merge with existing to preserve verify results
    existing_sources: dict[str, dict] = {}
    existing_sources_path = DATA_DIR / "sources.csv"
    if existing_sources_path.exists():
        with existing_sources_path.open(newline="", encoding="utf-8") as f:
            for row in csv.DictReader(f):
                existing_sources[row["source_id"]] = row

    with existing_sources_path.open("w", newline="", encoding="utf-8") as f:
        w = csv.DictWriter(f, fieldnames=SOURCE_COLUMNS)
        w.writeheader()
        for s in source_map.values():
            existing = existing_sources.get(s.source_id, {})
            w.writerow({
                "source_id": s.source_id,
                "url": s.url,
                # Preserve verified status/dates; default to unverified for new sources
                "retrieved_at": existing.get("retrieved_at", s.retrieved_at),
                "wayback_url": existing.get("wayback_url", s.wayback_url),
                "status": existing.get("status", s.status),
            })

    # Write claims.csv
    with (DATA_DIR / "claims.csv").open("w", newline="", encoding="utf-8") as f:
        w = csv.DictWriter(f, fieldnames=CLAIM_COLUMNS)
        w.writeheader()
        for c in all_claims:
            w.writerow({
                "claim_id": c.claim_id,
                "entity_id": c.entity_id,
                "predicate": c.predicate,
                "value": c.value,
                "source_ids": ";".join(c.source_ids),
                "confidence": c.confidence,
                "retrieved_at": c.retrieved_at,
            })

    # Write quarantine.csv
    if quarantine:
        qf = DATA_DIR / "quarantine.csv"
        keys = list(quarantine[0].keys())
        with qf.open("w", newline="", encoding="utf-8") as f:
            w = csv.DictWriter(f, fieldnames=keys)
            w.writeheader()
            w.writerows(quarantine)

    print("\nMigration complete:")
    for k, v in stats.items():
        print(f"  {k}: {v}")
    print(f"\nFiles written to {DATA_DIR}/")
    return stats


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--apply", action="store_true")
    parser.add_argument("--backup", action="store_true")
    parser.add_argument("--csv", type=Path,
                        default=ROOT / "datasets.csv")
    args = parser.parse_args()
    migrate(args.csv, apply=args.apply, backup=args.backup)


if __name__ == "__main__":
    main()
